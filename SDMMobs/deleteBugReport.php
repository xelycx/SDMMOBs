<?php
// Process delete operation after confirmation
if(isset($_POST["bug_id"]) && !empty($_POST["bug_id"]))
{
    // Include config file
    require_once "controller/BugReportController.php";
    
	$brc = new BugReportController();
	$brc->bugreport->set_id($_POST["bug_id"]);
	$brc->bugreport->DeleteBugReport();
}
else
{
    // Check existence of id parameter
    if(empty(trim($_GET["bug_id"])))
	{
        // URL doesn't contain id parameter. Redirect to error page
        header("location: error.php");
        exit();
    }
}
